export interface CosmicFile {
  url: string
  imgix_url: string
}

export interface CosmicObject {
  id: string
  slug: string
  title: string
  content?: string
  metadata?: Record<string, unknown>
  type?: string
  created_at?: string
  modified_at?: string
}

export interface Author extends CosmicObject {
  type?: 'authors'
  metadata?: {
    name?: string
    bio?: string
    headshot?: CosmicFile
  }
}

export interface Category extends CosmicObject {
  type?: 'categories'
  metadata?: {
    name?: string
    description?: string
  }
}

export interface Post extends CosmicObject {
  type?: 'posts'
  metadata?: {
    headline?: string
    content?: string
    featured_image?: CosmicFile
    author?: Author
    category?: Category
  }
}

export interface CosmicResponse<T> {
  objects: T[]
  total: number
  limit?: number
  skip?: number
}